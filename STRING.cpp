#include <iostream>
using namespace std;

int main() {
    string characterName = "pedro";
    int characterAge = 18;

    cout << "There once a man named " << characterName << endl;
    cout << "He was " << characterAge << " years old" << endl;

    characterName = "Jordan";
    characterAge = 54;

    cout << "He liked the name " << characterName << endl;
    cout << "But did not like being " << characterAge << endl;

    return 0;
}
